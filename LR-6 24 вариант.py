''' Лабораторная работа №6
Задание состоит из двух частей.  
1 часть – написать программу в соответствии со своим вариантом задания. 
2 часть – усложнить написанную программу, введя по своему усмотрению в условие минимум одно ограничение на характеристики объектов и целевую функцию для оптимизации решения. 
Вариант 24.
У няни неограниченное количество  фруктов К разных названий (ф1,…фК).
Сформировать (вывести) все возможные варианты меню полдника (1 фрукт) для ребенка на неделю.
'''
import random

#-------------------1 часть-------------------

print('1 часть')

# список фруктов
fruits = ['пельсин','яблоко','груша' ,'банан', 'киви', 'ананас', 'виноград', 'манго', 'персик']

# формируем все возможные варианты меню полдника на неделю
days = ['понедельник', 'вторник', 'среда', 'четверг', 'пятница', 'суббота', 'воскресенье']

for day in days:
    # выбираем случайный фрукт из списка, который еще не был выбран на этой неделе
    unique_fruits = list(set(fruits))
    fruit = random.choice(unique_fruits)
    # удаляем выбранный фрукт из списка, чтобы он не повторялся в другой день
    fruits.remove(fruit)
    # выводим меню полдника на день
    print('{0}: {1}'.format(day, fruit))


#-------------------2 часть-------------------

print('2 часть')

fruits_calories = [('апельсин', 47),('яблоко', 52),('груша', 57),('банан', 89),('киви', 61),('ананас', 50),('виноград', 69),('манго', 60),('персик', 39)]

# формируем все возможные варианты меню полдника на неделю
days = ['понедельник', 'вторник', 'среда', 'четверг', 'пятница', 'суббота', 'воскресенье']

menu = {}
used_fruits = []

for day in days:
    # выбираем случайный фрукт из списка, который еще не был выбран на этой неделе
    unused_fruits = [f for f in fruits_calories if f[0] not in used_fruits]
    fruit = random.choice(unused_fruits)

    # добавляем выбранный фрукт в меню и помечаем его как использованный
    menu[day] = fruit[0]
    used_fruits.append(fruit[0])

# выводим меню полдника на неделю с калорийностью
for day, fruit in menu.items():
    cal = next(f[1] for f in fruits_calories if f[0] == fruit)
    print('{0}: {1} ({2} ккал)'.format(day, fruit, cal))

# сортируем список фруктов по убыванию калорийности
fruits_calories.sort(key=lambda x: x[1], reverse=True)

# выводим самый калорийный фрукт
print('Самый калорийный фрукт на неделе: {0} ({1} ккал)'.format(fruits_calories[0][0], fruits_calories[0][1]))
